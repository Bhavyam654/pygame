import turtle
t=turtle.Turtle()
t.color("yellow")
t.begin_fill()
t.circle(70)
t.end_fill()
t.setheading(90)
t.forward(80)
t.left(90)
t.forward(45)
t.color("black")
t.left(90)
t.begin_fill()
t.circle(10)
t.end_fill()
t.left(90)
t.up()
t.forward(70)
t.right(90)
t.down()
t.begin_fill()
t.circle(10)
t.end_fill()
t.up()
t.forward(40)
t.down()
for circle in range(20):
    t.forward(5)
    t.right(10)
t.hideturtle()    
turtle.done()